package nttdata.javat1.game;

public class Ball {
	private int posX;
	private int posY;
	
	/**
	 * Constructor de la clase Ball
	 */
	public Ball() {
		super();
		this.posX = 0;
		this.posY = 0;
	}

	public int getPosX() {
		return posX;
	}

	public void setPosX(int posX) {
		this.posX = posX;
	}

	public int getPosY() {
		return posY;
	}

	public void setPosY(int posY) {
		this.posY = posY;
	}
	
	
}
